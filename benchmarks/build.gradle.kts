import org.gradle.api.JavaVersion.VERSION_1_8

plugins {
    java
    id("me.champeau.jmh") version "0.7.3"
}

// Must equal [package].name in rust-lib/Cargo.toml. cargo emits the jar as <crateName>-<hash>.jar.
val crateName = "bench_rust"

repositories {
    mavenCentral()
}

java {
    sourceCompatibility = VERSION_1_8
    targetCompatibility = VERSION_1_8
}

jmh {
    jmhVersion = "1.37"
}

// Defines which rustc_codegen_jvm build compiles rust-lib.
// Defaults to the parent repo but can point at a build in any unrelated folder via -PrustcJvmHome=… ,
// a `rustcJvmHome=…` line in gradle.properties, or the RUSTC_JVM_HOME env var.
val rustcJvmHome: File = file(
    (findProperty("rustcJvmHome") as String?)
        ?: System.getenv("RUSTC_JVM_HOME")
        ?: rootDir.parentFile.absolutePath,
)

val osName = System.getProperty("os.name").lowercase()
val backendLib = when {
    osName.contains("win") -> "rustc_codegen_jvm.dll"
    osName.contains("mac") -> "librustc_codegen_jvm.dylib"
    else -> "librustc_codegen_jvm.so"
}

val codegenBackend = rustcJvmHome.resolve("target/release/$backendLib")
val javaLinker = rustcJvmHome.resolve("java-linker/target/release/java-linker")
val libraryJar = rustcJvmHome.resolve("library/build/libs/library-0.1.0.jar")
val r8Jar = rustcJvmHome.resolve("vendor/r8.jar")
val proguardCfg = rustcJvmHome.resolve("proguard/default.pro")

val cargoConfig = layout.projectDirectory.file("rust-lib/.cargo/config.toml")

val generateCargoConfig by tasks.registering {
    group = "build"
    description = "Writes rust-lib/.cargo/config.toml pointing cargo at the configured backend."
    inputs.property("codegenBackend", codegenBackend.invariantSeparatorsPath)
    inputs.property("javaLinker", javaLinker.invariantSeparatorsPath)
    inputs.property("libraryJar", libraryJar.invariantSeparatorsPath)
    inputs.property("r8Jar", r8Jar.invariantSeparatorsPath)
    inputs.property("proguardCfg", proguardCfg.invariantSeparatorsPath)
    outputs.file(cargoConfig)
    doLast {
        val out = cargoConfig.asFile
        out.parentFile.mkdirs()
        out.writeText(
            """
            # GENERATED by the Gradle build (generateCargoConfig) — do not edit.
            # Points cargo at the rustc_codegen_jvm backend selected via rustcJvmHome.
            [build]
            rustflags = [
              "-Z", "codegen-backend=${codegenBackend.invariantSeparatorsPath}",
              "-C", "linker=${javaLinker.invariantSeparatorsPath}",
              "-C", "link-args=${libraryJar.invariantSeparatorsPath} --r8-jar ${r8Jar.invariantSeparatorsPath} --proguard-config ${proguardCfg.invariantSeparatorsPath}"
            ]

            [profile.debug]
            panic = "abort"

            [profile.release]
            panic = "abort"
            rustflags = [
              "-C", "link-args=--release"
            ]
            """.trimIndent() + "\n",
        )
    }
}

val rustJar = layout.buildDirectory.file("rust-libs/$crateName.jar")

val buildRustLib by tasks.registering(Exec::class) {
    group = "build"
    description = "Compiles the Rust benchmark library to a JVM jar via cargo."
    dependsOn(generateCargoConfig)
    workingDir = file("rust-lib")
    commandLine("cargo", "build", "--release")

    inputs.dir("rust-lib/src")
    inputs.file("rust-lib/Cargo.toml")
    inputs.file(cargoConfig)
    inputs.files(codegenBackend, javaLinker, libraryJar, r8Jar, proguardCfg).withPropertyName("backend")
    outputs.file(rustJar)

    // cargo emits <crateName>-<hash>.jar; copy the freshest one to the stable staged path.
    doLast {
        val deps = file("rust-lib/target/release/deps")
        val produced = deps.listFiles { _, name -> name.startsWith(crateName) && name.endsWith(".jar") }
            ?.maxByOrNull { it.lastModified() }
            ?: throw GradleException("cargo build produced no $crateName-*.jar in $deps")
        val dest = rustJar.get().asFile
        dest.parentFile.mkdirs()
        produced.copyTo(dest, overwrite = true)
    }
}

dependencies {
    // The staged jar flows into the jmh compile and runtime classpaths
    jmhImplementation(files(rustJar).builtBy(buildRustLib))
}
